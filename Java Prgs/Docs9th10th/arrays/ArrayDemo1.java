class ArrayDemo1
{
    public static void main()
    {
        int N[]={1,3,4,5,6,7,8,9,10};
        int s= N[0]*N[0];
        double r = 1.0/N[3];
        double c = Math.pow(N[7],3);
        System.out.println("square of 1st no. " + s);
        System.out.println("reciprocal of 4th no. " + r);
        System.out.println("cube of 8th no. " + c);
    }
}

        