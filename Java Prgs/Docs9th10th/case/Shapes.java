public class Shapes
{
    public static void main(int n)
    {
        switch(n)
        {
            case 1 : System.out.println("point"); break;
            case 2 : System.out.println("line"); break;
            case 3 : System.out.println("triangle"); break;
            case 4 : System.out.println("Quadilateral"); break;
            case 5 : System.out.println("pentagon"); break;
            case 6 : System.out.println("hexagon"); break;
            case 7 : System.out.println("heptagon"); break;
            case 8 : System.out.println("octagon"); break;
            case 9 : System.out.println("nonagon"); break;
            case 10 : System.out.println("decagon"); break;
            default : System.out.println("invalid"); break;
        }
    }
}
            